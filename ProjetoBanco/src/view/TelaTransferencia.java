     public void ConsultaSaldo(){
       String sql = "Select Saldo From conta WHERE Numero = ? ";
       
       try {
           pst = conexao.prepareStatement(sql);
           pst.setInt(1, conta);
           rs = pst.executeQuery();
           
           if(rs.next()){  
               saldo = rs.getDouble(1);
               
               
           }
           
           
           
       } catch (Exception e) {
           System.out.println("Não achei");
       }
       
       
       
       
       
   }
   
   public void Retirando(){
       String sac = "Update conta SET Saldo = ? WHERE Numero = ?";
       
       try {
           pst = conexao.prepareStatement(sac);
           pst.setDouble(1, saldofinal);
           pst.setInt(2, conta);
           pst.executeUpdate();
           
           
           
           
       } catch (Exception e) {
       }
       
       
       
   }
   
   public void adicionando(){
        String adc = "Update conta SET Saldo = ? WHERE Numero = ?";
       
       try {
           pst = conexao.prepareStatement(adc);
           pst.setDouble(1, valordeposito);
           pst.setInt(2, Integer.parseInt(txtcontclientetransfer.getText()));
           pst.executeUpdate();
           
           
           
           
       } catch (Exception e) {
       }
       
       

       
   }
   
   
   public void contadestino(){
       String contad = "Select Id_Conta, Saldo FROM conta WHERE Numero = ?";
       
       try {
           pst = conexao.prepareStatement(contad);
           pst.setInt(1, Integer.parseInt(txtcontclientetransfer.getText()));
           rs = pst.executeQuery();
           
           if(rs.next()){
               idcontadestino = rs.getInt(1);
               saldocontadestino = rs.getDouble(2);
               
               
               
           }
           
           
       } catch (Exception e) {
       }
               
       
       
       
       
   }
   
   
   
   
public void pegandonome(){
    String peganome = "Select Nome From cliente WHERE Id_cliente";
    
    try {
        pst = conexao.prepareStatement(peganome);
        pst.setInt(1, idcontadestino);
        
        rs = pst.executeQuery();
        if(rs.next()){
            nomedestinatario = rs.getString(1);
            

        }
        
        
        
    } catch (Exception e) {
    }
    
    
    
    
    
}
    
