    private void BotaoTransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoTransferirActionPerformed
        if(txtcontclientetransfer.getText().equals("") && txtvalortransfer.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha os dados corretamente!");
            
            
        } else {
            ConsultaSaldo();
            if (saldo < 0 || saldo < Double.parseDouble(txtvalortransfer.getText())){
                JOptionPane.showMessageDialog(null, "Você não tem saldo suficiente!");
                
                
                
                
            } else {
                saldofinal = saldo - Double.parseDouble(txtvalortransfer.getText());
                Retirando();
                contadestino();
                valordeposito = saldocontadestino + Double.parseDouble(txtvalortransfer.getText());
                adicionando();
                pegandonome();

                JOptionPane.showMessageDialog(null, "Transferência realizada com sucesso!! \n"
                        + "Destinatário: "+ nomedestinatario);
                txtcontclientetransfer.setText("");
                txtvalortransfer.setText("");
            
        }
            
        }


    }//GEN-LAST:event_BotaoTransferirActionPerformed

